number1 = input("Please choose a number")
number2 = input("Please choose one more number")
number_actual1 = int(number1)
number_actual2 = int(number2)
sum1 = number_actual1 + number_actual2
difference1 = number_actual1 - number_actual2
product1 = number_actual1 * number_actual2
quotient1 = number_actual1 / number_actual2
print(f"The sum of your two numbers is {sum1}")
print(f"The difference of your two numbers is {difference1}")
print(f"The product of your two numbers is {product1}")
print(f"The quotient of your two numbers is {quotient1}")